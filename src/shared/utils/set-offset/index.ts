export const setOffsetValue = (key: string) => {
  switch (key) {
    case "1":
      return 0;
    case "2":
      return 151;
    case "3":
      return 251;
    case "4":
      return 386;
    case "5":
      return 493;
    case "6":
      return 649;
    case "7":
      return 721;
    case "8":
      return 809;
    default:
      return 0;
  }
};
